	<h1> Use-Case </h1>
<p>
	Billy is constantly looking to increase his collection of name brand clothes, however
	with the competitive pricing across numerous online websites "ebay" seemed like the most
	suitable alternative. The ability to resell and bid on a wide range of products allows for
	a more flexible market better suited to his needs.
</p>
	<h1> Interaction-Flow</h1>
		<ul>
			<li> Billy types out the url for ebay</li>
			<li> After loading the site he types in the name of a popular brand</li>
			<li> He then sorts the drop menu to the article of clothing he is looking for</li>
			<li> He then browses the listings in order to find the best deal </li>
			<li> After adding the item to his cart he will then continue to the checkout</li>
			<li> Billy will now proceed to fill out his credentials, adding his mothers credit card</li>
			<li> The purchase was finalized and now Billy will wait to receive any notifications on the delivery status </li>
		</ul>
