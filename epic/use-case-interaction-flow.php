	<h1> Use-Case </h1>
<div>
<li><strong> Title: </strong> Purchasing and Bidding on high-end clothing </li>
<li><strong> Description: </strong> Billy wants to buy a versace headband and hightops </li>
<li><strong> Name of User/Role: </strong> Billy enjoys fashion culture as well as buying and re-selling designer clothes </li>
<li><strong> Usage Precondition: </strong> Must have active account with billing information set-up </li>
<li><strong> Usage Postcondition: </strong> Items are saved to cart for convenience and possible bidding </li>
</div>
	<h1> Interaction-Flow</h1>
		<ul>
			<li> Billy types out the url for ebay</li>
			<li> The server loads the site to ebay's main page </li>
			<li> Billy types in the item name in the search bar and presses enter </li>
			<li> The server loads the results and displays them on another page </li>
			<li> He then browses the listings and adds a few items to his cart </li>
			<li> The server displays the item information icluding the total cost</li>
			<li> Billy will now proceed to fill out his credentials, adding his mothers credit card and completing the purchase</li>
			<li> The server confirms the account information in order to approve the request</li>
		</ul>
